#include <stdio.h>
#include <stdlib.h>

int main() {

    int a = 5;
    float f = 3.4522;
    double b = 4.2362;
    char chr = '+';

    printf("a = %d \n", a);
    printf("Float decimal = %f \n", f);
    printf("Double decimal = %lf \n", b);
    printf("Char = %c \n", chr);

    return EXIT_SUCCESS;
}