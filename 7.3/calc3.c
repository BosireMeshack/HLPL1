#include <stdio.h>
#include <stdlib.h>

int main(int argc, char const *argv[])
{
      while(1)
    {
            int a, b;
            char op;
            if (scanf("%d %c%d\n", &a, &op, &b) == EOF)
            
            {
                if (a == 0 || b == 0)
                {
                    break;
                }
            };
    };

 

    return EXIT_SUCCESS;
}
